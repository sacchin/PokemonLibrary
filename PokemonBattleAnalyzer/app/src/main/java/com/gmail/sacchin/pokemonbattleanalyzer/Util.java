    public static Bitmap createImage(PBAPokemon p, float scale, Resources resource){
        Bitmap image = BitmapFactory.decodeResource(resource, p.getResourceId());
        if(image == null){
            Log.e("createImage", p.getJname() + " - " + p.getResourceId());
            return null;
        }
        Matrix matrix = new Matrix();
        matrix.postScale(scale / (float)image.getWidth(), scale / (float)image.getHeight());
        image = Bitmap.createBitmap(image, 0, 0, image.getWidth(),image.getHeight(), matrix, true);
        return image;
    }
